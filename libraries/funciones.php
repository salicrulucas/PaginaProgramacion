function catalogo_x_marcas(string $marca) : array{
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $m) {
        if($m['linea']== $marca){
            //agregar elementos al array(push)
            $resultado[]= $m;
        }
    }
    return $resultado;
}


/**
 * Devuelve los datos de una moto en particular
 */

